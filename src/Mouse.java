public class Mouse {
    private String name;
    private String food;
    private String enemy;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFood() {
        return food;
    }

    public void setFood(String food) {
        this.food = food;
    }

    public String getEnemy(){
       return enemy;
    }
    public void setEnemy(String enemy){
        this.enemy = enemy;
    }

}